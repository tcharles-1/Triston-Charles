def days_of_week(number):
	if number == 0:
		print("Sunday")
	elif number == 1:
		print("Monday")
	elif number == 2:
		print("Tuesday")
	elif number == 3:
		print("Wednesday")
	elif number == 4:
		print("Thursday")
	elif number == 5:
		print("Friday")
	elif number == 6:
		print("Saturday")
	else:
		print("Invalid. Number between 0 and 6 only.")
